package dayonetothree;

public class arrways {
    public static void main(String[] args) {
        int[] arr = {1, 3, 5, 7, 9};
        printArray(arr);
    }

    public static void printArray(int[] arr)

    {
        for (int i = 0; i < arr.length; i++)
        {
            System.out.println(arr[i]);
        }
    }
}
